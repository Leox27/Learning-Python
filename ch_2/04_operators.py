# Arithmetic operators
a = 7
b = 4
c = a + b #We can use  multiple operators like -,*,/ etc.

print(c)


# Assignment operator
a = 4-2
print(a)

b = 6

b += 2    # Increment the value of b by 2 and then assign it to b
print(b) 

c = 3-4   # c = -1
c -= 2    # Decrement the value of c by 2 and then assign it to c
print(c)

# Comparison operators
d = 4==3
print(d)

d = 4!=3
e = 4!=4
print(d) 
print(e) 

# Logical operator
o = True or False # OR operatir
print(o)

o = True and False
print(o)

print("True or False is", True or False) # AND operator
print("False or True is", False or True)
print("True or True is", True or True)
print("False or False is", False or False)

print("True and False is", True and False) # OR operator
print("False and True is", False and True)
print("True and True is", True and True)
print("False and False is", False and False)


print(not(True))  # NOT Operator True to False
print(not(False)) # NOT operator False to True







